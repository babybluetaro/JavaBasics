package org.example.class8;

public class E4_2Darrays {
    public static void main(String[] args) {
// print 7 row of 3 stars
        for (int j=0;j<7;j++){  //the outer loop

            for (int i =0; i<3;i++){//the inner loop
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
